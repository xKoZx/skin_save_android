package com.example.skin_save;

public class User {
    private String name;
    private String email;




    public User(String name, String email) {
        this.name = name;
        this.email = email;

    }

    public String getname() {
        return name;

    }

    public void setname(String name) {
        this.name = name;

    }

    public String getemail() {
        return email;

    }

    public void setemail(String name) {
        this.email = email;

    }

}

